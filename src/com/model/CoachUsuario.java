package com.model;

public class CoachUsuario {
	String strNombre;
	String strNombreUser;
	String strApellido;
	String strHorario;
	public String getStrNombre() {
		return strNombre;
	}
	public void setStrNombre(String strNombre) {
		this.strNombre = strNombre;
	}
	public String getStrNombreUser() {
		return strNombreUser;
	}
	public void setStrNombreUser(String strNombreUser) {
		this.strNombreUser = strNombreUser;
	}
	public String getStrApellido() {
		return strApellido;
	}
	public void setStrApellido(String strApellido) {
		this.strApellido = strApellido;
	}
	public String getStrHorario() {
		return strHorario;
	}
	public void setStrHorario(String strHorario) {
		this.strHorario = strHorario;
	}
	
	
}
