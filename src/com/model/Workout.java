package com.model;

public class Workout {
	String nombre;
	String strWorkout;
	String strDescripcion;
	int intSets;
	int intReps;
	
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getStrWorkout() {
		return strWorkout;
	}
	public void setStrWorkout(String strWorkout) {
		this.strWorkout = strWorkout;
	}
	public String getStrDescripcion() {
		return strDescripcion;
	}
	public void setStrDescripcion(String strDescripcion) {
		this.strDescripcion = strDescripcion;
	}
	public int getIntSets() {
		return intSets;
	}
	public void setIntSets(int intSets) {
		this.intSets = intSets;
	}
	public int getIntReps() {
		return intReps;
	}
	public void setIntReps(int intReps) {
		this.intReps = intReps;
	}
	
}
